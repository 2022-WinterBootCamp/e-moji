def emotions(result) :
    if result == "neutral" :
            return 0
    elif result == "disgust" :
            return 1
    elif result == "fear" :
            return 2
    elif result == "happy" :
            return 3
    elif result == "angry" :
            return 4
    elif result == "sad" :
            return 5
    elif result == "surprise" :
            return 6